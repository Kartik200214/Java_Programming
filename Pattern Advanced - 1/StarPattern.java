
public class StarPattern 
{
    public static void main(String args[]) 
    {
        System.out.println();
        for(int line = 1;line<=10;line++)
        {
            for(int star =1;star <=line; star++)
            {
                System.out.print("*\t");
            }
            System.out.println();

        }
       
      
        
    }
    
}
